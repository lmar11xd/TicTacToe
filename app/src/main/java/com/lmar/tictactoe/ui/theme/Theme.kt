@Composable
fun TicTacToeTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    val colors = when {
        useDarkTheme -> darkScheme
        else -> lightScheme
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        val activity = view.context as Activity

        SideEffect {
            val window = activity.window
            val insetsController = WindowCompat.getInsetsController(window, view)

            // Asegurar que el contenido ocupa toda la pantalla
            WindowCompat.setDecorFitsSystemWindows(window, false)

            // Activar fondo en la barra de estado y navegación
            window.setFlags(
                WindowManager.LayoutParams.FLAG_DRAWS_SYSTEM_BAR_BACKGROUNDS,
                WindowManager.LayoutParams.FLAG_DRAWS_SYSTEM_BAR_BACKGROUNDS
            )

            // Alternativa a navigationBarColor en Android 14+
            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.UPSIDE_DOWN_CAKE) { // API 34+
                window.isNavigationBarContrastEnforced = false
            }

            // Configurar la apariencia de los íconos de la barra de estado y navegación
            insetsController.isAppearanceLightStatusBars = !useDarkTheme
            insetsController.isAppearanceLightNavigationBars = !useDarkTheme
        }
    }

    MaterialTheme(
        colorScheme = colors,
        typography = AppTypography,
        content = content
    )
}

